<article>
  <p class="text-console mx-2 text-justify">
  L'invite de commande est cette petite fenêtre noire, où vous pouvez uniquement taper du texte et afficher uniquement du texte. Il vous permet de parcourir le contenu de votre disque dur, de manipuler des dossiers et des fichiers et de démarrer des programmes. En d'autres termes, contrôlez l'ordinateur en tapant du texte (ligne de commande) au lieu d'utiliser la souris.<br>
  Par exemple, la command line vous permet de taper certaines commandes qui ne sont pas accessibles via l'interface graphique Windows. Les informaticiens et les utilisateurs expérimentés l'utilisent également pour écrire de petits programmes (scripts) afin d'automatiser des tâches répétitives. Pour afficher la liste des commandes dans l'invite de commandes, tapez help dans la fenêtre et appuyez sur Entrée pour vérifier. Il affiche ensuite l'ensemble des commandes, chacune avec une brève description. Pour trouver toutes les options d'une commande, veuillez taper help dir (par exemple).<br>
  C'est tout pour aujourd'hui : entrez exit et validez !
