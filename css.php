  <h5>Le CSS qu'est ce que c'est ?</h5>
<p class="card-text2 text-justify mt-3">
CSS (Cascading Style Sheet) est un langage informatique utilisé pour décrire la représentation et le style des documents HTML et XML. L'histoire de cette langue remonte aux années 90 et est principalement utilisée pour le développement de sites Web. Contrairement au HTML, où on utilise des balises de début et de fin, CSS s'identifie avec un point avant sa classe, une accolade avant son attribut et deux points avant sa valeur.
</p>
<p class="card-text2 text-justify mt-3">
L'avantage d'utiliser des fichiers CSS pour le format de site Web est que vous pouvez modifier tous les titres du site Web en même temps en modifiant une seule partie du fichier CSS. Sans ce fichier CSS, il est nécessaire de modifier chaque titre sur chaque page du site (c'est presque impossible pour un grand site avec des milliers de pages).
</p>
<p class="card-text2 text-justify mt-3">
  D'autres avantages sont également perceptibles. Par exemple, vous pouvez créer des feuilles de style spécifiques pour les documents imprimés, ce qui vous permet de supprimer tous les effets de style et toutes les parties inutiles lors de l'impression. De même, les feuilles de style peuvent être utilisées par les utilisateurs de téléphones portables, ce qui permet de mieux gérer les formats, notamment les petits écrans de ces appareils.</p>
